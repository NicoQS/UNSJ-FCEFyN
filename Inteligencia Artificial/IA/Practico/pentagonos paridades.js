function* range(start, end, size, triangular = false){
	const range = Array(size).fill(start);

	if(triangular){
		while(range[0] <= end){
			yield range.slice();
			range[size - 1]++;
			
			for(let i = size - 1; i > 0; i--){
				if(range[i] > end) range[i - 1]++;
			}

			for(let i = 0; i < size; i++){
				if(range[i] > end) range[i] = range[i - 1];
			}
		}
	}else while(range[0] <= end){
		yield range.slice();
		range[size - 1]++;
		for(let i = size - 1; i > 0; i--){
			if(range[i] > end){
				range[i] = start;
				range[i - 1]++;
			}
		}
	}
}

